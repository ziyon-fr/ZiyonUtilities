import Foundation

public extension TimeInterval {
    
    /// 1 hour = 60 \* 60 seconds
    static let hour = TimeInterval(60 * 60)
    /// 1 day = 24 \* 60 \* 60 seconds
    static let day = TimeInterval(24 * 60 * 60)
}
